package Modelo;

public class Factura {
    private int idFactura;
    private Cliente idCliente;

    public Factura() {
        this.idFactura = idFactura;
        this.idCliente = idCliente;
    }

    public int getIdFactura() {
        return idFactura;
    }

    public Cliente getIdCliente() {
        return idCliente;
    }
}
